package operations;

public class CharArithDemo {
    public static void main(String[] args) {
        char ch;
        ch = 'x';
        System.out.println("ch  содержит " + ch);

        ch ++;
        System.out.println("ch after incremente: " + ch);

        ch = 90;
        System.out.println("ch after reinitialization: " + ch);

    }
}
